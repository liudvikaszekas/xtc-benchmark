"""
Helper script to run Hungarian matching on generated OpenPSG scene graphs.

This script loads the scene graphs generated by generate_openpsg_graphs.py
and runs the semantic graph matching algorithm on each image pair.
"""

import json
import argparse
import os
import sys
import tempfile
from pathlib import Path
from typing import Dict
import numpy as np

# Avoid CUDA issues by using CPU for sentence transformers
os.environ['CUDA_VISIBLE_DEVICES'] = ''
# Avoid tokenizer parallelism issues when forking child processes
os.environ['TOKENIZERS_PARALLELISM'] = 'false'

ROOT_DIR = Path(__file__).resolve().parents[2]
GRAPH_MATCHING_DIR = Path(__file__).resolve().parent / 'graph_matching'
if str(GRAPH_MATCHING_DIR) not in sys.path:
    sys.path.insert(0, str(GRAPH_MATCHING_DIR))

from hungarian import (
    load_graph,
    load_graph_from_data,
    compute_text_embeddings_for_graph,
    semantic_graph_matching,
    convert_hungarian_graph_to_nx,
)

def load_scene_graphs(json_path: str) -> Dict:
    """Load scene graphs from JSON file."""
    print(f"Loading scene graphs from {json_path}...")
    with open(json_path, 'r') as f:
        data = json.load(f)
    return data
from sentence_transformers import SentenceTransformer


def match_single_image(image_id, graphs, model, strict_labels, high_recall, node_sim_weight, edge_sim_weight):
    try:
        from hungarian import (
            load_graph_from_data,
            compute_text_embeddings_for_graph,
            semantic_graph_matching,
            convert_hungarian_graph_to_nx,
        )
        
        pred_graph = load_graph_from_data(graphs['prediction'], source_name=f"pred_{image_id}")
        gt_graph = load_graph_from_data(graphs['ground_truth'], source_name=f"gt_{image_id}")

        compute_text_embeddings_for_graph(pred_graph, model)
        compute_text_embeddings_for_graph(gt_graph, model)

        pred_nx = convert_hungarian_graph_to_nx(pred_graph)
        gt_nx = convert_hungarian_graph_to_nx(gt_graph)

        match_results = semantic_graph_matching(
            gt_nx, pred_nx, model, 
            node_sim_weight=node_sim_weight, 
            edge_sim_weight=edge_sim_weight
        )

        if match_results:
            precision, recall, f1, matched_edges, unmatched_gt, unmatched_pred, \
            matched_nodes, unmatched_gt_nodes, unmatched_pred_nodes = match_results
            
            result = {
                'precision': float(precision),
                'recall': float(recall),
                'f1_score': float(f1),
                'num_matched_edges': len(matched_edges),
                'num_unmatched_gt_edges': len(unmatched_gt),
                'num_unmatched_pred_edges': len(unmatched_pred),
                'num_matched_nodes': len(matched_nodes),
                'num_unmatched_gt_nodes': len(unmatched_gt_nodes),
                'num_unmatched_pred_nodes': len(unmatched_pred_nodes),
                'num_gt_edges': len(graphs['ground_truth'].get('edges', [])),
                'num_pred_edges': len(graphs['prediction'].get('edges', [])),
                'num_gt_nodes': len(graphs['ground_truth'].get('nodes', [])),
                'num_pred_nodes': len(graphs['prediction'].get('nodes', [])),
                'matched_node_pairs': matched_nodes,
                'unmatched_gt_nodes': unmatched_gt_nodes,
                'unmatched_pred_nodes': unmatched_pred_nodes,
                'matched_edge_pairs': matched_edges,
                'unmatched_gt_edges': unmatched_gt,
                'unmatched_pred_edges': unmatched_pred,
            }
            return result
        return None
    except Exception as e:
        print(f"Error processing image {image_id}: {e}")
        return None

def match_scene_graphs(
    scene_graphs_data: Dict,
    model: SentenceTransformer,
    output_path: str,
    strict_labels: bool = True,
    high_recall_mode: bool = True,
    node_sim_weight: float = 0.7,
    edge_sim_weight: float = 0.3,
) -> Dict:
    """
    Run Hungarian matching on all image pairs sequentially.
    """
    results = {}
    images = scene_graphs_data['images']
    
    print(f"\nMatching {len(images)} image pairs sequentially...")
    print(f"Strict label matching: {strict_labels}")
    print(f"High recall mode: {high_recall_mode}")
    print(f"Weights: alpha={node_sim_weight}, beta={edge_sim_weight}")
    
    for i, (image_id, graphs) in enumerate(images.items()):
        result = match_single_image(image_id, graphs, model, strict_labels, high_recall_mode, node_sim_weight, edge_sim_weight)
        results[image_id] = result
        if (i + 1) % 10 == 0 or (i + 1) == len(images):
            print(f"Progress: [{i+1}/{len(images)}] images processed.")

    return results


def compute_aggregate_metrics(results: Dict) -> Dict:
    """Compute aggregate metrics across all images."""
    valid_results = [r for r in results.values() if r is not None]
    
    if not valid_results:
        return {}
    
    # Compute means for Hungarian algorithm
    mean_precision = np.mean([r['precision'] for r in valid_results])
    mean_recall = np.mean([r['recall'] for r in valid_results])
    mean_f1 = np.mean([r['f1_score'] for r in valid_results])
    
    # Compute totals
    total_matched_edges = sum(r['num_matched_edges'] for r in valid_results)
    total_gt_edges = sum(r['num_gt_edges'] for r in valid_results)
    total_pred_edges = sum(r['num_pred_edges'] for r in valid_results)
    
    # Compute overall precision/recall (micro-average)
    total_fp = total_pred_edges - total_matched_edges
    total_fn = total_gt_edges - total_matched_edges
    
    overall_precision = total_matched_edges / total_pred_edges if total_pred_edges > 0 else 0.0
    overall_recall = total_matched_edges / total_gt_edges if total_gt_edges > 0 else 0.0
    overall_f1 = 2 * (overall_precision * overall_recall) / (overall_precision + overall_recall) \
                 if (overall_precision + overall_recall) > 0 else 0.0
    
    return {
        'num_images': len(valid_results),
        'mean_precision': float(mean_precision),
        'mean_recall': float(mean_recall),
        'mean_f1': float(mean_f1),
        'overall_precision': float(overall_precision),
        'overall_recall': float(overall_recall),
        'overall_f1': float(overall_f1),
        'total_matched_edges': total_matched_edges,
        'total_gt_edges': total_gt_edges,
        'total_pred_edges': total_pred_edges,
        'total_false_positives': total_fp,
        'total_false_negatives': total_fn,
    }


def main():
    parser = argparse.ArgumentParser(
        description="Run Hungarian matching on OpenPSG scene graphs"
    )
    parser.add_argument(
        '--input',
        type=str,
        required=True,
        help='Path to scene graphs JSON file (from generate_openpsg_graphs.py)'
    )
    parser.add_argument(
        '--output',
        type=str,
        default=None,
        help='Output path for matching results JSON (default: input_path with _matched.json)'
    )
    parser.add_argument(
        '--model',
        type=str,
        default='all-MiniLM-L6-v2',
        help='SentenceTransformer model for embeddings'
    )
    parser.add_argument(
        '--high-recall',
        action='store_true',
        default=True,
        help='Use high recall mode (forces all GT edges to be matched)'
    )
    parser.add_argument(
        '--no-high-recall',
        action='store_true',
        help='Disable high recall mode'
    )
    parser.add_argument(
        '--strict-labels',
        action='store_true',
        default=True,
        help='Use strict exact matching for node and edge labels'
    )
    parser.add_argument(
        '--no-strict-labels',
        action='store_true',
        help='Disable strict label matching'
    )
    
    parser.add_argument(
        '--alpha',
        type=float,
        default=0.7,
        help='Node similarity weight (alpha)'
    )
    parser.add_argument(
        '--beta',
        type=float,
        default=0.3,
        help='Edge similarity weight (beta)'
    )
    
    args = parser.parse_args()
    
    # Determine output path
    if args.output is None:
        input_path = Path(args.input)
        args.output = str(input_path.parent / f"{input_path.stem}_matched.json")
    
    # Load scene graphs
    scene_graphs = load_scene_graphs(args.input)
    
    print(f"\nMatching mode model tag: {args.model}")

    print(f"Loading embedding model: {args.model}")
    model = SentenceTransformer(args.model)
    
    # Determine high recall mode
    high_recall = args.high_recall and not args.no_high_recall
    strict_labels = args.strict_labels and not args.no_strict_labels
    
    # Run matching
    results = match_scene_graphs(
        scene_graphs,
        model=model,
        output_path=args.output,
        strict_labels=strict_labels,
        high_recall_mode=high_recall,
        node_sim_weight=args.alpha,
        edge_sim_weight=args.beta,
    )
    
    # Compute aggregate metrics
    aggregate = compute_aggregate_metrics(results)
    
    # Prepare output
    output_data = {
        'metadata': scene_graphs.get('metadata', {}),
        'matching_config': {
            'model': args.model,
            'high_recall_mode': high_recall,
            'strict_label_matching': strict_labels,
        },
        'aggregate_metrics': aggregate,
        'per_image_results': results
    }
    
    # Save results
    with open(args.output, 'w') as f:
        json.dump(output_data, f, indent=2)
    
    # Print summary
    print(f"\n{'='*70}")
    print("Matching Complete!")
    print(f"{'='*70}")
    print(f"Results saved to: {args.output}")
    
    if aggregate:
        print(f"\nAggregate Metrics ({aggregate['num_images']} images):")
        print(f"\n{'='*70}")
        print("HUNGARIAN ALGORITHM RESULTS:")
        print(f"{'='*70}")
        print(f"  Mean Precision:    {aggregate['mean_precision']:.4f}")
        print(f"  Mean Recall:       {aggregate['mean_recall']:.4f}")
        print(f"  Mean F1:           {aggregate['mean_f1']:.4f}")
        print(f"\n  Overall Precision: {aggregate['overall_precision']:.4f}")
        print(f"  Overall Recall:    {aggregate['overall_recall']:.4f}")
        print(f"  Overall F1:        {aggregate['overall_f1']:.4f}")
        
        print(f"\n{'='*70}")
        print(f"Edge Counts:")
        print(f"{'='*70}")
        print(f"  Total Matched:     {aggregate['total_matched_edges']}")
        print(f"  Total GT:          {aggregate['total_gt_edges']}")
        print(f"  Total Predicted:   {aggregate['total_pred_edges']}")
        print(f"  False Positives:   {aggregate['total_false_positives']}")
        print(f"  False Negatives:   {aggregate['total_false_negatives']}")


if __name__ == '__main__':
    main()
